import org.springframework.web.bind.annotation.RestController;

@RestController
public class HelloController {

	@GetMapping("/")
	public String index() {
		return "Greetings from Spring Boot!";
	}

	@GetMapping("/test/{id}")
	public String test(@PathVariable("id") String id) {

		return "the id is "+ id ;
	}

}
